        private static string GetDeviceNameMacro(string deviceName)
        {
            string text;
            map.TryGetValue(deviceName.ToLowerInvariant(), out text);

            if (string.IsNullOrEmpty(text) == false)
                return text;

            if (deviceName.Length < 4)
                return string.Empty;

            deviceName = deviceName.Substring(4); //strip out the AT32 and retry

            map.TryGetValue(deviceName.ToLowerInvariant(), out text);
            return string.IsNullOrEmpty(text) ? string.Empty : text;
        }

        public static  IEnumerable<string> GetSymbols (string deviceName, Arch arch)
        {
            var deviceSymbol = GetDeviceNameMacro(deviceName);

            var symbols = new List<string>() {deviceSymbol};
            
            if (arch == Arch.AVR)
                symbols.AddRange(avr8CommonCompilerDefinedSymbols);

            return symbols;
        }

